# Напишите программу, которая генерирует случайное число от 1 до 100 включительно
# и дает пользователю 10 попыток, чтобы угадать это число. Программа должна
# подсказывать, больше или меньше загаданное число. После завершения игры
# программа оценивает, насколько хорошая интуиция у игрока.

import random

secret_ = random.randint(1, 100)
popitka = 10

print("Я загадал число от 1 до 100. Попробуйте угадать за 10 попыток!")

for popitka in range(1, popitka + 1):
    while True:
        vvod = input("Попытка " + str(popitka) + ". Введите число: ")
        if vvod.isdigit():
            vvod = int(vvod)
            break
        print("Ошибка! Введите целое число.")

    if vvod < secret_:
        print("Не угадали! Загаданное число больше.")
    elif vvod > secret_:
        print("Не угадали! Загаданное число меньше.")
    else:
        print("Поздравляю! Вы угадали число " + str(secret_) + " за " + str(popitka) + " попыток!")
        break
else:
    print("Увы, попытки закончились! Загаданное число было " + str(secret_) + ".")




