# #2- Напишите программу, которая запрашивает у пользователя целое число и определяет,
# # является ли оно палиндромом. Число является палиндромом, если оно читается
# # одинаково слева направо и справа налево.

vvod = input("Введите число: ")

if vvod == vvod[::-1]:
    print("Число является палиндромом.")
else:
    print("Число НЕ является палиндромом.")

