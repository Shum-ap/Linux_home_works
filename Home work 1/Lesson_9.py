'''5. Учет доходов и расходов
Задача: Напишите программу, которая принимает на вход положительные и
отрицательные значения и останавливается, получив ноль. Программа должна рассчитать
сумму доходов (положительные) и расходов (отрицательные), а также итоговый баланс.
Пример вывода: Введите доход, расход или '0' для получения итога: 15000
Введите доход, расход или '0' для получения итога: -5000
Введите доход, расход или '0' для получения итога: -3000
Введите доход, расход или '0' для получения итога: 0
Доходы: 15000
Расходы: 8000
Итоговый баланс: 7000'''

income = 0  # Сумма доходов
expenses = 0  # Сумма расходов

while True:
    amount = int(input("Введите доход, расход или '0' для получения итога: "))
    if amount == 0:
        break
    elif amount > 0:
        income += amount  # Считаем доходы
    else:
        expenses += abs(amount)  # Считаем расходы

balance = income - expenses  # Итоговый баланс

# Вывод результатов
print(f"Доходы: {income}")
print(f"Расходы: {expenses}")
print(f"Итоговый баланс: {balance}")



