''' 9..Напишите программу, которая считывает четырёхзначное число и меняет местами
первую и последнюю цифры.
Пример вывода:
Введите четырёхзначное число: 1234
Число после изменения: 4231'''

n = int(input("Введите четырёхзначное число: "))

if 1000 <= n <= 9999:  # Проверяем, что число четырёхзначное
    last = n % 10       # Последняя цифра
    first = n // 1000   # Первая цифра
    middle = (n // 10) % 100  # Средние цифры

    new_number = last * 1000 + middle * 10 + first  # Собираем новое число
    print("Число после изменения:", new_number)
else:
    print("Ошибка: введите четырёхзначное число!")
