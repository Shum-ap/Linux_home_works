'''Одинаковый сдвиг
Задача: Напишите программу, которая проверяет, имеют ли две строки одинаковые
сдвиги между парами символов в разных словах на одинаковых позициях.
Например, 'acc' имеет сдвиги 2, 0, и 'egg' имеет такие же.
Пример вывода: Введите первую строку: acc
Введите вторую строку: egg
Одинаковый сдвиг? True'''

def check_shift(str1, str2):
    if len(str1) != len(str2):
        return False
    shifts = []
    for i in range(1, len(str1)):
        shift1 = ord(str1[i]) - ord(str1[i-1])
        shift2 = ord(str2[i]) - ord(str2[i-1])
        shifts.append((shift1, shift2))
    return all(shift1 == shift2 for shift1, shift2 in shifts)

str1 = input("Введите первую строку: ")
str2 = input("Введите вторую строку: ")

result = check_shift(str1, str2)
print("Одинаковый сдвиг?", result)
