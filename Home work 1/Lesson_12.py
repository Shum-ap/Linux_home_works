'''Текст палиндром
Задача: Напишите программу, которая принимает строку и проверяет, является ли
она палиндромом, игнорируя всё кроме букв русского и английского алфавита.
Пример вывода: Введите строку: А роза упала на лапу Азора
Палиндром? True
'''

text = input("Введите строку: ")

# Оставляем только буквы русского и английского алфавита
filtered_text = ""
for char in text:
    if char.isalpha():
        filtered_text += char.lower()

# Проверяем, является ли строка палиндромом
is_palindrome = filtered_text == filtered_text[::-1]

print("Палиндром?", is_palindrome)



