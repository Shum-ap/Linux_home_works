'''Проверка скобок
Задача: Напишите программу, которая проверяет правильность расстановки
круглых скобок в строке.
Пример вывода: Введите строку: (a+b)*(c-d)
Скобки расставлены правильно? True
Введите строку: (a+b)(*((c-d)
Скобки расставлены правильно? False'''

def check_brackets(s):
    count = 0
    for char in s:
        if char == '(':
            count += 1
        elif char == ')':
            count -= 1
        if count < 0:
            return False
    return count == 0

text = input("Введите строку: ")
print("Скобки расставлены правильно?", check_brackets(text))


