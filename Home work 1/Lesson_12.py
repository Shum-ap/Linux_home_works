number = input("Введите число: ")

even_digits = ""
odd_digits = ""

for digit in number:
    if int(digit) % 2 == 0:
        even_digits += digit
    else:
        odd_digits += digit

# Если строки пустые, заменяем их на 0
even_digits = "0" if not even_digits else even_digits
odd_digits = "0" if not odd_digits else odd_digits

print("Число из четных цифр:", even_digits)
print("Число из нечетных цифр:", odd_digits)


